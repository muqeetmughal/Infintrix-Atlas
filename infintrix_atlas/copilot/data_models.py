
from pydantic import BaseModel, Field
from typing import List, Optional, Literal


# ---------- Breakdown ----------
class GeneralResponse(BaseModel):
    message: str


        
class SuggestedTask(BaseModel):
    subject: str
    task_type: Literal["Task"]
    reason: str


